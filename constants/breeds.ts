import { BreedId } from '@/store/types';

// 0 = transparent, 1 = primary color, 2 = secondary color, 3 = dark detail (eyes/nose)
type PixelValue = 0 | 1 | 2 | 3;
export type PixelGrid = PixelValue[][];

export type BreedConfig = {
  id: BreedId;
  name: string;
  colors: {
    primary: string;
    secondary: string;
    dark: string;
  };
  grid: PixelGrid;
};

function p(rows: string[]): PixelGrid {
  return rows.map(row =>
    row.split('').map(ch => {
      if (ch === '1') return 1;
      if (ch === '2') return 2;
      if (ch === '3') return 3;
      return 0;
    }) as PixelValue[]
  );
}

